function noteline (minv, maxv)
    freq = [293.66, 311.13, 329.63, 349.23, 369.99, 392.00, 415.30, 440.00, 466.16, 493.88, 523.25, 554.37,587.33,622.25,659.25,698.46,739.99,783.99,830.61,880.00,932.33,987.77,1046.50,1108.73	];
    note = ["D4","D#4/Eb4","E4","F4"," F#4/Gb4","G4"," G#4/Ab4 	","A4"," A#4/Bb4 	","B4","C5"," C#5/Db5 	","D5"," D#5/Eb5 	","E5","F5"," F#5/Gb5 	","G5"," G#5/Ab5 	","A5"," A#5/Bb5 	","B5","C6"," C#6/Db6 	"];
